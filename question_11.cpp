class Solution{
public:	
	/* Function to replace every element with the
	next greatest element */
	void nextGreatest(int arr[], int n) {
	    int temp1=-1;
	    int temp2;
	    for(int i=n-1;i>=0;i--){
	        temp2=arr[i];
	        arr[i]=temp1;
	        temp1=max(temp1,temp2);
	    }
	}


};

